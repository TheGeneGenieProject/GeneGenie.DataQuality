namespace GeneGenie.DataQuality.Data
{
    /// <summary>
    /// Years that UK census' were taken.
    /// </summary>
    public enum UkCensusYears
    {
        /// <summary>
        /// The 1801 census, destroyed except for a few records.
        /// </summary>
        Census1801 = 1801,

        /// <summary>
        /// The 1811 census, destroyed except for a few records.
        /// </summary>
        Census1811 = 1811,

        /// <summary>
        /// The 1821 census, destroyed except for a few records.
        /// </summary>
        Census1821 = 1821,

        /// <summary>
        /// The 1831 census, destroyed except for a few records.
        /// </summary>
        Census1831 = 1831,

        /// <summary>
        /// The 1841 census, destroyed except for a few records.
        /// </summary>
        Census1841 = 1841,

        /// <summary>
        /// The 1851 census, publicly available.
        /// </summary>
        Census1851 = 1851,

        /// <summary>
        /// The 1861 census, publicly available.
        /// </summary>
        Census1861 = 1861,

        /// <summary>
        /// The 1871 census, publicly available.
        /// </summary>
        Census1871 = 1871,

        /// <summary>
        /// The 1881 census, publicly available.
        /// </summary>
        Census1881 = 1881,

        /// <summary>
        /// The 1891 census, publicly available.
        /// </summary>
        Census1891 = 1891,

        /// <summary>
        /// The 1901 census, publicly available.
        /// </summary>
        Census1901 = 1901,

        /// <summary>
        /// The 1911 census, publicly available.
        /// </summary>
        Census1911 = 1911,

        /// <summary>
        /// The 1921 census expected to be released by The National Archives in January 2022.
        /// </summary>
        Census1921 = 1921,

        /// <summary>
        /// The 1931 census, destroyed during WW2.
        /// </summary>
        Census1931 = 1931,

        /// <summary>
        /// WW2 1939 register, publicly available.
        /// Technically not a census, but effectively a smaller version of what would have been the 1941 census.
        /// </summary>
        Census1939 = 1939,

        /// <summary>
        /// The 1951 census, records not publicly accessible.
        /// </summary>
        Census1951 = 1951,

        /// <summary>
        /// The 1961 census, records not publicly accessible.
        /// </summary>
        Census1961 = 1961,

        /// <summary>
        /// The 1971 census, records not publicly accessible.
        /// </summary>
        Census1971 = 1971,

        /// <summary>
        /// The 1981 census, records not publicly accessible.
        /// </summary>
        Census1981 = 1981,

        /// <summary>
        /// The 1991 census, records not publicly accessible.
        /// </summary>
        Census1991 = 1991,

        /// <summary>
        /// The 2001 census, records not publicly accessible.
        /// </summary>
        Census2001 = 2001,

        /// <summary>
        /// The 2011 census, records not publicly accessible.
        /// </summary>
        Census2011 = 2011,
    }
}
